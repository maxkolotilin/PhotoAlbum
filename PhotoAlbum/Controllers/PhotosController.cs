        }

        // GET api/<controller>/5
        public IEnumerable<string> Get(int albumId)
        {
            return db.Photos.Where(p => p.AlbumId == albumId)
                .Select(p => p.Guid).ToList().Select(g => $"/Photo/{ g }");
        }

